class Solution {
    public String solution(String myString) {
        String answer = myString.toUpperCase();
        // String =  Java의 내장 클래스이며, 문자열을 저장하고 조작하는 데 사용
        // answer = 변수명
        // myString.toUpperCase = myString에 있는 스펠링을 대문자로 바꿔줌
        return answer;
        // answer을 return함
    }
}